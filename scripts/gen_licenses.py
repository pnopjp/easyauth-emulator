#!/usr/bin/env python3
"""
Generate THIRD_PARTY_LICENSES from installed package metadata.
Run after updating Python dependencies to keep the file current.

Usage:
    python scripts/gen_licenses.py           # generate THIRD_PARTY_LICENSES
    python scripts/gen_licenses.py --check   # exit(1) if file is out of date
"""
import argparse
import difflib
import importlib.metadata
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
REQUIREMENTS = ROOT / "requirements.txt"
OUTPUT = ROOT / "THIRD_PARTY_LICENSES"
LICENSES_DIR = ROOT / "scripts" / "licenses"

# License texts for entries with no locally-inspectable artifact to read from
# (no dist-info like a pip package, no file bundled in an image like the
# manylinux system libraries) live as plain files under scripts/licenses/,
# not inline here, so this file stays logic-only.
