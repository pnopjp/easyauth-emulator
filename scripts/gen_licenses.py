def _system_library_entries() -> list[dict]:
    data = json.loads(MANYLINUX_LICENSES_FILE.read_text(encoding="utf-8"))
    entries = []
    for pkg, meta in SYSTEM_LIBRARY_METADATA.items():
        pkg_data = data[pkg]
        entries.append({
            "name": meta["display_name"].format(version=pkg_data["version"]),
            "license_id": meta["license_id"].format(version=pkg_data["version"]),
            "homepage": meta["homepage"],
            "note": meta["note"],
            "license_text": pkg_data["license_text"],
        })
    return entries

OPENSSL_APACHE_LICENSE_TEXT = (LICENSES_DIR / "openssl-apache-2.0.txt").read_text(encoding="utf-8").strip("\n")


def _cryptography_openssl_entry() -> dict | None:
    """Detect the OpenSSL version statically linked into the installed `cryptography`
    package, by reading the SBOM it ships in its own dist-info. Returns None if
    `cryptography` isn't installed, or its SBOM doesn't declare a vendored OpenSSL
    (e.g. a future release that drops OpenSSL entirely)."""
    try:
        files = importlib.metadata.files("cryptography")
    except importlib.metadata.PackageNotFoundError:
        return None
    if not files:
        return None
    sbom_file = next((f for f in files if Path(str(f)).name == "sbom.json"), None)
    if sbom_file is None:
        return None
    try:
        data = json.loads(sbom_file.read_text(encoding="utf-8"))
    except Exception:
        return None
    openssl = next(
        (c for c in data.get("components", []) if (c.get("name") or "").lower() == "openssl"),
        None,
    )
    if openssl is None:
        return None
    version = openssl.get("version", "unknown")
    cryptography_version = importlib.metadata.version("cryptography")
    return {
        "name": f"OpenSSL {version} (statically linked into the cryptography package)",
        "license_id": "Apache-2.0",
        "homepage": "https://www.openssl.org/",
        "note": (
            f"Statically linked inside the cryptography {cryptography_version} package's "
            "compiled extension, on all platforms. This is a separate, independently "
            "versioned copy of OpenSSL from the one listed above for the Linux build -- "
            "detected here from cryptography's own bundled SBOM (sboms/sbom.json) at "
            "THIRD_PARTY_LICENSES generation time, so it tracks whatever OpenSSL version "
            "the installed cryptography release vendors."
        ),
        "license_text": OPENSSL_APACHE_LICENSE_TEXT,
    }


HEADER = """\
Third-Party Licenses
====================

This file lists the open-source components used by easyauth-emulator.
Each component retains its original license.

The easyauth-emulator project itself is licensed under the Apache License 2.0
(see LICENSE).

"""

SEP = "=" * 72


def _read_requirements() -> list[str]:
    """Read package names from requirements.txt (strips version specifiers)."""
    packages = []
    for line in REQUIREMENTS.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        # Strip version specifier (==, >=, <=, ~=, !=, >, <)
        for op in ("==", ">=", "<=", "~=", "!=", ">", "<"):
            if op in line:
                line = line[: line.index(op)].strip()
                break
        packages.append(line)
    return packages


def _find_license_text(pkg: str) -> str:
    files = importlib.metadata.files(pkg)
    if not files:
        return ""
    candidates = [
        f for f in files
        if "dist-info" in str(f)
        and any(
            seg in Path(str(f)).name.upper()
            for seg in ("LICENSE", "LICENCE", "COPYING", "COPYRIGHT")
        )
    ]
    for candidate in candidates:
        try:
            text = candidate.read_text(encoding="utf-8")
            if text.strip():
                return text.strip()
        except Exception:
            continue
    return ""


def _license_id(meta: importlib.metadata.PackageMetadata) -> str:
    value = meta.get("License-Expression") or meta.get("License") or ""
    if value:
        return value
    classifiers = meta.get_all("Classifier") or []
    ids = [
        c.split(" :: ")[-1]
        for c in classifiers
        if c.startswith("License ::")
    ]
    return ", ".join(ids)


def _python_package_section(pkg: str) -> str:
    meta = importlib.metadata.metadata(pkg)
    name = meta["Name"]
    version = meta["Version"]
    homepage = meta.get("Home-page") or ""
    license_id = _license_id(meta)
    license_text = _find_license_text(pkg)

    lines = [SEP, f"{name} {version}"]
    if license_id:
        lines.append(f"License: {license_id}")
    if homepage:
        lines.append(f"Homepage: {homepage}")
    lines += [SEP, "", license_text if license_text else "(License text not available in package metadata)"]
    return "\n".join(lines)


def _static_section(entry: dict) -> str:
    lines = [
        SEP,
        entry["name"],
        f"License: {entry['license_id']}",
        f"Homepage: {entry['homepage']}",
        f"Note: {entry['note']}",
        SEP,
        "",
        entry["license_text"],
    ]
    return "\n".join(lines)


def _generate() -> str:
    packages = _read_requirements()
    sections = [_python_package_section(pkg) for pkg in packages]
    sections += [_static_section(entry) for entry in _system_library_entries()]
    cryptography_openssl = _cryptography_openssl_entry()
    if cryptography_openssl is not None:
        sections.append(_static_section(cryptography_openssl))
    sections.append(_static_section(OAUTH2_PROXY_ENTRY))
    return HEADER + "\n\n\n".join(sections) + "\n"


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--check", action="store_true", help="Exit with error if THIRD_PARTY_LICENSES is out of date")
    args = parser.parse_args()

    content = _generate()

    if args.check:
        if not OUTPUT.exists():
            print(f"[gen_licenses] ERROR: {OUTPUT.name} does not exist. Run without --check to generate it.", file=sys.stderr)
            sys.exit(1)
        current = OUTPUT.read_text(encoding="utf-8")
        if current != content:
            diff = difflib.unified_diff(
                current.splitlines(keepends=True),
                content.splitlines(keepends=True),
                fromfile=f"{OUTPUT.name} (current)",
                tofile=f"{OUTPUT.name} (expected)",
            )
            print(f"[gen_licenses] ERROR: {OUTPUT.name} is out of date. Run `python scripts/gen_licenses.py` and commit the result.", file=sys.stderr)
            print(f"[gen_licenses] Diff:", file=sys.stderr)
            sys.stderr.writelines(diff)
            sys.exit(1)
        print(f"[gen_licenses] {OUTPUT.name} is up to date.")
    else:
        OUTPUT.write_text(content, encoding="utf-8", newline="\n")
        print(f"[gen_licenses] Written: {OUTPUT}")


if __name__ == "__main__":
    main()
